#include "libft.h"

size_t	ft_strlcpy(char *dest, char *src, size_t size)
{
	size_t	i;
	size_t	src_len;
	size_t	j;

	if (dest == NULL || src == NULL)
		return (0);
	i = 0;
	src_len = ft_strlen(src);
	j = src_len;
	if (size > 0)
	{
		while (src[i] != '\0' && i < size - 1)
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (j);
}
